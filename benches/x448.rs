//! Divan micro-benchmarks for X448 key agreement (RFC 7748).
//!
//! `cryptoxide` 0.5 does not provide X448, so (unlike the X25519 bench) there is
//! no cross-implementation comparison here.
//!
//! Run with:
//!
//! ```text
//! cargo bench --bench x448 --features x448
//! ```

fn main() {
    divan::main();
}

#[cfg(feature = "x448")]
mod x448 {
    use divan::{black_box, Bencher};
    use eccoxide::protocol::x448::{x448, x448_base};

    // Two arbitrary secret scalars (RFC 7748 section 6.2).
    const SK_A: [u8; 56] = [
        0x9a, 0x8f, 0x49, 0x25, 0xd1, 0x51, 0x9f, 0x57, 0x75, 0xcf, 0x46, 0xb0, 0x4b, 0x58, 0x00,
        0xd4, 0xee, 0x9e, 0xe8, 0xba, 0xe8, 0xbc, 0x55, 0x65, 0xd4, 0x98, 0xc2, 0x8d, 0xd9, 0xc9,
        0xba, 0xf5, 0x74, 0xa9, 0x41, 0x97, 0x44, 0x89, 0x73, 0x91, 0x00, 0x63, 0x82, 0xa6, 0xf1,
        0x27, 0xab, 0x1d, 0x9a, 0xc2, 0xd8, 0xc0, 0xa5, 0x98, 0x72, 0x6b,
    ];
    const SK_B: [u8; 56] = [
        0x1c, 0x30, 0x6a, 0x7a, 0xc2, 0xa0, 0xe2, 0xe0, 0x99, 0x0b, 0x29, 0x44, 0x70, 0xcb, 0xa3,
        0x39, 0xe6, 0x45, 0x37, 0x72, 0xb0, 0x75, 0x81, 0x1d, 0x8f, 0xad, 0x0d, 0x1d, 0x69, 0x27,
        0xc1, 0x20, 0xbb, 0x5e, 0xe8, 0x97, 0x2b, 0x0d, 0x3e, 0x21, 0x37, 0x4c, 0x9c, 0x92, 0x1b,
        0x09, 0xd1, 0xb0, 0x36, 0x6f, 0x10, 0xb6, 0x51, 0x73, 0x99, 0x2d,
    ];

    /// Public-key derivation: `[scalar]·basepoint`.
    #[divan::bench]
    fn derive_public(bencher: Bencher) {
        bencher.bench(|| x448_base(black_box(&SK_A)));
    }

    /// Full Diffie-Hellman: `[scalar]·peer_public`.
    #[divan::bench]
    fn agreement(bencher: Bencher) {
        let peer = x448_base(&SK_B);
        bencher.bench(|| x448(black_box(&SK_A), black_box(&peer)));
    }
}
